#!/bin/bash

    # Zeichen für die Animation (Ändere nach Wunsch)
    BAR_CHARS=("▁" "▂" "▃" "▄" "▅" "▆" "▇" "█")

    while true; do
        # Prüfe, ob ein Player aktiv ist
        STATUS=$(playerctl status 2>/dev/null)
        
        if [ "$STATUS" == "Playing" ]; then
            # Erstelle eine zufällige Balken-Animation
            OUTPUT=""
            for i in {1..8}; do
                OUTPUT+="${BAR_CHARS[$((RANDOM % ${#BAR_CHARS[@]}))]} "
            done
            echo "{\"text\": \"$OUTPUT\", \"tooltip\": \"Musik läuft\", \"class\": \"playing\"}"
        
        elif [ "$STATUS" == "Paused" ]; then
            echo "{\"text\": \"⏸\", \"tooltip\": \"Musik pausiert\", \"class\": \"paused\"}"
        
        else
            echo "{\"text\": \"\", \"tooltip\": \"Kein Player aktiv\", \"class\": \"stopped\"}"
        fi

        sleep 0.05  # Geschwindigkeit der Animation (kann angepasst werden)
    done
